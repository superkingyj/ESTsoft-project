// for 
for (let i = 0; i < 5; i++){
    console.log(i);
}


// while
let i = 0;
while (i<5){
    console.log(i);
    i++;
}

// if-else
let x = 0;
if ( x > 0){
    console.log("x는 양수입니다.");
} else if (x < 0){
    console.log("x는 음수입니다.");
} else{
    console.log("x는 0입니다.");
}

// switch
let color = "black";
switch (color) {
    case "red": console.log("빨간색입니다."); break;
    case "blue": console.log("파란색입니다."); break;
    case "black": console.log("검정색입니다."); break;
    default: console.log("리스트에 없습니다.."); break;
}


let animal = 'cat';

switch (animal) {
    case "dog": console.log("Bark"); break;
    case "cat": console.log("Meow"); break;
    case "bird": console.log("Tweet"); break;
    default: console.log("Unknown animal");
}






